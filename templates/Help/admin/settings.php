<h2>General</h2>
<dl class="row dl-stack">
  <dt class="col-sm-3">Site name</dt>
  <dd class="col-sm-9">Shown in the page title and outbound emails. Defaults to <em>eQSL Card</em>. Change it to match your club or callsign if this is a station-specific install.</dd>

  <dt class="col-sm-3">Share base URL</dt>
  <dd class="col-sm-9">The root URL used to build public share links (<code>https://your-domain/qsl/{slug}</code>). Must include the scheme. Change this if the site lives at a sub-path or uses a CDN URL.</dd>
</dl>

<h2>Uploads</h2>
<dl class="row dl-stack">
  <dt class="col-sm-3">Max upload (MB)</dt>
  <dd class="col-sm-9">Maximum file size accepted for background image uploads, in megabytes. Set to 0 to disable user uploads entirely. Keep this below the PHP <code>upload_max_filesize</code> directive — the PHP limit always wins.</dd>

  <dt class="col-sm-3">Default background</dt>
  <dd class="col-sm-9">The fallback background image used when an operator picks "site default" at render time. Upload an image here (JPEG, PNG, or WebP, max 2000×1500 px after auto-resize). If no custom background is uploaded, the bundled demo image is used. Attribution fields (author, licence) are stored and printed on cards that use this background.</dd>
</dl>

