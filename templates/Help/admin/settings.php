<h2>Card credit line</h2>
<dl class="row dl-stack">
  <dt class="col-sm-3">Credit template</dt>
  <dd class="col-sm-9">A free-text string printed as a micro-credit on rendered cards. Supports the placeholder <code>{site_name}</code>. Default: <em>Generated by {site_name}</em>. Set this to your club's name or website.</dd>

  <dt class="col-sm-3">Card retention (days)</dt>
  <dd class="col-sm-9">When set to a positive integer, the <em>Expire old cards</em> action on <a href="/admin/cleanup">/admin/cleanup</a> soft-deletes user cards older than this many days. Set to 0 to disable retention entirely (cards live forever). The soft-delete hides cards from the user's library but doesn't immediately free disk — run <em>Prune orphan uploads</em> after expiry to reclaim space.</dd>
</dl>

<h2>Callsign auto-complete</h2>
<p>See the dedicated article: <a href="/help/logging/autocomplete">Callsign auto-complete</a>. This section summarises the settings pane:</p>
<ul>
  <li>Check <strong>Enable callsign auto-complete</strong> to turn it on.</li>
  <li>Tick each provider you want active and drag them into priority order. The local CSV directory always appears at the top and is always the first provider tried, regardless of drag order.</li>
  <li>A QRZ API key field appears when QRZ is selected.</li>
</ul>

<h2>Security</h2>
<dl class="row dl-stack">
  <dt class="col-sm-3">Bypass rate limit for private IPs</dt>
  <dd class="col-sm-9">When checked, requests from RFC-1918 addresses (10.x, 172.16.x, 192.168.x) and localhost skip the login-rate-limit. Useful for internal testing; harmless on a public internet install since those IPs are never routed.</dd>
</dl>

