<h2>Email (SMTP)</h2>
<p>Configure SMTP to enable password-reset emails. Without SMTP, users who forget their password have no self-service recovery path.</p>
<dl class="row dl-stack">
  <dt class="col-sm-3">SMTP host</dt>
  <dd class="col-sm-9">The mail server hostname, e.g. <code>smtp.mailgun.org</code> or <code>mail.your-isp.com</code>.</dd>

  <dt class="col-sm-3">SMTP port</dt>
  <dd class="col-sm-9">587 for STARTTLS (recommended), 465 for SSL/TLS, 25 for unencrypted (avoid).</dd>

  <dt class="col-sm-3">SMTP user / password</dt>
  <dd class="col-sm-9">Credentials for your mail account. Stored in the database (app_settings table), not in a config file.</dd>

  <dt class="col-sm-3">From address</dt>
  <dd class="col-sm-9">The address that appears in the <em>From</em> field of outbound emails, e.g. <code>noreply@your-domain.com</code>.</dd>
</dl>

