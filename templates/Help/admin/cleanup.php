<h2>Guest-card purge</h2>
<p>Guest cards are generated by anonymous visitors through the public render form. They're temporary by nature — once the visitor has downloaded their card, the server copy isn't needed. <strong>Purge guest cards</strong> deletes guest-owned card rows and their PNG/WebP/PDF files on disk, for cards older than the selected cutoff.</p>

