<h2>How the page works</h2>
<p>Every section on the cleanup page first shows a <strong>count preview</strong> — how many rows would be affected at the current cutoff — so you can verify the scope before clicking the action button. The cutoff is adjustable via the <strong>Days</strong> slider at the top; the preview updates as you change it.</p>

